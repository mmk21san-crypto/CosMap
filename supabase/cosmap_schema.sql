begin;

create extension if not exists pgcrypto;

-- ===== Master tables =====
create table if not exists public.avatar_presets (
  id text primary key,
  emoji text not null
);

create table if not exists public.event_areas (
  id smallserial primary key,
  name text not null unique
);

-- ===== User related =====
create table if not exists public.profiles (
  id uuid primary key default gen_random_uuid(),
  -- Optional link to Supabase Auth user
  auth_user_id uuid unique references auth.users (id) on delete set null,
  username text not null unique,
  bio text not null default '',
  avatar_preset_id text references public.avatar_presets (id),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.profile_social_links (
  profile_id uuid primary key references public.profiles (id) on delete cascade,
  x_handle text not null default '',
  instagram_handle text not null default '',
  tiktok_handle text not null default ''
);

create table if not exists public.user_follows (
  follower_id uuid not null references public.profiles (id) on delete cascade,
  following_id uuid not null references public.profiles (id) on delete cascade,
  created_at timestamptz not null default now(),
  primary key (follower_id, following_id),
  check (follower_id <> following_id)
);

-- ===== Event related =====
create table if not exists public.events (
  id bigint generated by default as identity primary key,
  name text not null,
  venue text not null,
  event_tag text not null check (event_tag in ('野外', '室内', '大規模')),
  start_at timestamptz not null,
  end_at timestamptz not null,
  ticket_label text not null,
  ticket_price_yen integer check (ticket_price_yen is null or ticket_price_yen >= 0),
  is_free boolean not null default false,
  url text,
  description text not null default '',
  dressing_room_available boolean not null default false,
  photo_allowed boolean not null default true,
  created_at timestamptz not null default now(),
  check (end_at > start_at),
  check ((is_free and ticket_price_yen is null) or (not is_free))
);

create index if not exists idx_events_start_at on public.events (start_at);

create table if not exists public.event_media (
  id bigint generated by default as identity primary key,
  event_id bigint not null references public.events (id) on delete cascade,
  sort_order integer not null default 1,
  media_type text not null default 'color' check (media_type in ('color', 'image_url')),
  color_hex text,
  image_url text,
  check (color_hex is null or color_hex ~ '^#[0-9A-Fa-f]{6}$'),
  check (
    (media_type = 'color' and color_hex is not null and image_url is null)
    or
    (media_type = 'image_url' and image_url is not null)
  ),
  unique (event_id, sort_order)
);

create table if not exists public.event_posts (
  id bigint generated by default as identity primary key,
  event_id bigint not null references public.events (id) on delete cascade,
  author_profile_id uuid not null references public.profiles (id) on delete cascade,
  emoji text not null,
  body text not null,
  like_count integer not null default 0 check (like_count >= 0),
  created_at timestamptz not null default now()
);

create index if not exists idx_event_posts_event_id_created_at
  on public.event_posts (event_id, created_at desc);

create table if not exists public.event_attendances (
  profile_id uuid not null references public.profiles (id) on delete cascade,
  event_id bigint not null references public.events (id) on delete cascade,
  status text not null default 'going' check (status in ('going', 'interested', 'cancelled')),
  created_at timestamptz not null default now(),
  primary key (profile_id, event_id)
);

create table if not exists public.user_event_locations (
  profile_id uuid not null references public.profiles (id) on delete cascade,
  event_id bigint not null references public.events (id) on delete cascade,
  area_id smallint not null references public.event_areas (id),
  location_text text not null default '',
  updated_at timestamptz not null default now(),
  primary key (profile_id, event_id)
);

create index if not exists idx_user_event_locations_event_id_updated_at
  on public.user_event_locations (event_id, updated_at desc);

-- ===== Studio related =====
create table if not exists public.studios (
  id text primary key,
  name text not null,
  area text not null,
  studio_type text not null check (studio_type in ('貸切', 'シェア')),
  hourly_label text not null,
  hourly_rate_yen integer check (hourly_rate_yen is null or hourly_rate_yen >= 0),
  url text,
  created_at timestamptz not null default now()
);

create table if not exists public.studio_media (
  id bigint generated by default as identity primary key,
  studio_id text not null references public.studios (id) on delete cascade,
  sort_order integer not null default 1,
  media_type text not null default 'color' check (media_type in ('color', 'image_url')),
  color_hex text,
  image_url text,
  check (color_hex is null or color_hex ~ '^#[0-9A-Fa-f]{6}$'),
  check (
    (media_type = 'color' and color_hex is not null and image_url is null)
    or
    (media_type = 'image_url' and image_url is not null)
  ),
  unique (studio_id, sort_order)
);

create table if not exists public.studio_posts (
  id bigint generated by default as identity primary key,
  studio_id text not null references public.studios (id) on delete cascade,
  author_profile_id uuid not null references public.profiles (id) on delete cascade,
  emoji text not null,
  body text not null,
  like_count integer not null default 0 check (like_count >= 0),
  created_at timestamptz not null default now()
);

create index if not exists idx_studio_posts_studio_id_created_at
  on public.studio_posts (studio_id, created_at desc);

